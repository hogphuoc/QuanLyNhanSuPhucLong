    public void hienthiphongban(String search) throws SQLException {
        Phongban dspb[] = new PhongbanRepository().phongbanarray(search);
        DefaultTableModel model = (DefaultTableModel)tbpb.getModel();
        model.setRowCount(0);
        for(Phongban pb : dspb){
            Object row[] = {pb.getMapb(), pb.getTenpb(), pb.getSodtpb()};
            model.addRow(row);
        }
    }
