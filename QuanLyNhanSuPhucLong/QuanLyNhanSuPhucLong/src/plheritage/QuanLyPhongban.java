    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        int result = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa phòng này?", "Thông báo", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
        if(result == 0){
            try {
                PhongbanRepository khoaRepository = new PhongbanRepository();
                String makh = tfmpb.getText();
                if(khoaRepository.deletePhong(makh)){
                    JOptionPane.showMessageDialog(null, "Xóa phòng ban thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(null, "Xóa phòng ban thất bại", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }
                hienthiphongban("");
                Binding();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage(), "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            } catch (ArrayIndexOutOfBoundsException e){
            }
        }
    }//GEN-LAST:event_btndeleteActionPerformed

    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        String mapb,tenpb,sdt ,message;
        int stc;

        mapb = tfmpb.getText();
        tenpb = tftenph.getText();
        sdt=tfsdt.getText();
        
        
        Phongban Phongban = new Phongban(mapb, tenpb, sdt);
        
        try {
            PhongbanRepository pbRepository = new PhongbanRepository();
            // Thêm phòng ban mới
            if(this.action.equals("add")){
                message = pbRepository.addPhong(Phongban) ? "Thêm phòng thành công" : "Thêm phòng thất bại";
                JOptionPane.showMessageDialog(null, message, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
            // Sửa phòng ban
            else if(this.action.equals("edit")){
                message = pbRepository.editphong(Phongban) ? "Sửa phòng thành công" : "Sửa phòng thất bại";
                JOptionPane.showMessageDialog(null, message, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
            hienthiphongban("");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            ex.printStackTrace();
        }
        
        setEditable(false);
        btnadd.setEnabled(true);
        btnedit.setEnabled(true);
        btndelete.setEnabled(true);
        btnsave.setEnabled(false);
    }//GEN-LAST:event_btnsaveActionPerformed

