    public void Binding(){
        int selected;
        boolean co1, khong1, co2,khong2;
        
        selected = tbhoso.getSelectedRow();
        
        tfmhs.setText(tbhoso.getValueAt(selected, 0).toString());
        cbmnv.setSelectedItem(tbhoso.getValueAt(selected, 1));
        tftdhv.setText(tbhoso.getValueAt(selected, 2).toString());       
        co1 = tbhoso.getValueAt(selected, 3).toString().equals("Có") ? true : false;
        khong1 = tbhoso.getValueAt(selected, 3).toString().equals("Không") ? true : false;
        co2 = tbhoso.getValueAt(selected, 4).toString().equals("Có") ? true : false;
        khong2 = tbhoso.getValueAt(selected, 4).toString().equals("Không") ? true : false;
        rco1.setSelected(co1);
        rkhong1.setSelected(khong1);
        rco2.setSelected(co2);
        rkhong2.setSelected(khong2);
    }
    public void hienthihoso(String search) throws SQLException {
        HoSo dshs[] = new HoSoRepository().HoSoArray(search);
        DefaultTableModel model = (DefaultTableModel)tbhoso.getModel();
        model.setRowCount(0);
        for(HoSo hs : dshs){
            Object row[] = {hs.getMahs(),hs.getManv(),hs.getTrinhDo(),hs.getGiayKhamSucKhoe(),hs.getGiayKhaiSinh()};
            model.addRow(row);
        }
    }
    public void hienthiNhanVien() throws SQLException {
        NhanVien dsnv[] = new NhanVienRepository().NhanVienArray("");
        DefaultComboBoxModel model = (DefaultComboBoxModel)cbmnv.getModel();
        for(NhanVien nv : dsnv){
            model.addElement(new ComboboxText(nv.getManv(), nv.getHoten()));
        }
    }    
