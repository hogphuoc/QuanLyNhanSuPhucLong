    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        int result = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa hồ sơ này?", "Thông báo", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
        if(result == 0){
            try {
                HoSoRepository hsRepository = new HoSoRepository();
                String mahs = tfmhs.getText();
                if(hsRepository.deleteHoSo(mahs)){
                    JOptionPane.showMessageDialog(null, "Xóa hờ sơ thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(null, "Xóa hồ sơ thất bại", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }
                hienthihoso("");
                Binding();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage(), "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            } catch (ArrayIndexOutOfBoundsException e){
            }
        }
    }//GEN-LAST:event_btndeleteActionPerformed

    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        String mhs,mnv,tdhv, gks ,gksk,message;
        
        mhs = tfmhs.getText();
        mnv = cbmnv.getSelectedItem().toString();
        tdhv = tftdhv.getText();
        gksk = rco1.isSelected() ? "Có" : "Không";
        gks = rco2.isSelected() ? "Có" : "Không";
        
        HoSo hs= new HoSo(mhs, mnv, tdhv, gksk, gksk);
        
        try {
            HoSoRepository hsRepository = new HoSoRepository();
            // Thêm hồ sơ mới
            if(this.action.equals("add")){
                message = hsRepository.addHoSo(hs) ? "Thêm hồ sơ thành công" : "Thêm hồ sơ thất bại";
                JOptionPane.showMessageDialog(null, message, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
            // Sửa hồ sơ
            else if(this.action.equals("edit")){
                message = hsRepository.editHoSo(hs) ? "Sửa hồ sơ thành công" : "Sửa hồ sơ thất bại";
                JOptionPane.showMessageDialog(null, message, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
            hienthihoso("");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
        
        setEditable(false);
        btnadd.setEnabled(true);
        btnedit.setEnabled(true);
        btndelete.setEnabled(true);
        btnsave.setEnabled(false);
    }//GEN-LAST:event_btnsaveActionPerformed

