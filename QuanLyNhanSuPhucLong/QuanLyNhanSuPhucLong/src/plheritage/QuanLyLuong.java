    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        String pl,mnv,mbcc,luongcb,phucap,thue ,message;
        ComboboxText cbItem;
        
        pl = tfpl.getText();
        try {
            cbItem = (ComboboxText)cbmnv.getModel().getSelectedItem();
            mnv = cbItem.getValue();
            cbItem = (ComboboxText)cbmbcc.getModel().getSelectedItem();
            mbcc = cbItem.getValue();
        } catch (ClassCastException e) {
            mnv = cbmnv.getSelectedItem().toString();
            mbcc = cbmbcc.getSelectedItem().toString();
        }
        luongcb=tflcb.getText();
        phucap=tfpc.getText();
        thue=tfthue.getText();
        
        Luong l = new Luong(pl, mnv, mbcc, luongcb, phucap, thue);
        
        try {
            LuongRepository luongRepository = new LuongRepository();
            // Thêm phiếu lương mới
            if(this.action.equals("add")){
                message = luongRepository.addLuong(l) ? "Thêm phiếu lương thành công" : "Thêm phiếu lương thất bại";
                JOptionPane.showMessageDialog(null, message, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
            // Sửa phiếu lương
            else if(this.action.equals("edit")){
                message = luongRepository.editLuong(l) ? "Sửa lương thành công" : "Sửa lương thất bại";
                JOptionPane.showMessageDialog(null, message, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
            hienthiluong("");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
        setEditable(false);
        btnadd.setEnabled(true);
        btnedit.setEnabled(true);
        btndelete.setEnabled(true);
        btnsave.setEnabled(false);
    }//GEN-LAST:event_btnsaveActionPerformed

