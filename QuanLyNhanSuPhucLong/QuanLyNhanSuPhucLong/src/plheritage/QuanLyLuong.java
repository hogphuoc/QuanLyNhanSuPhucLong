    public void hienthiluong(String search) throws SQLException {
        Luong dshp[] = new LuongRepository().LuongnArray(search);
        DefaultTableModel model = (DefaultTableModel)tbluong.getModel();
        model.setRowCount(0);
        for(Luong l : dshp){
            Object row[] = {l.getPhieuLuong(),l.getMaNV(),l.getMaBCC(),l.getLuongcb(),l.getPhucap(),l.getThue()};
            model.addRow(row);
        }
    }
    
public void hienthinhanvien() throws SQLException {
 NhanVien dsnv[] = new NhanVienRepository().NhanVienArray("");
        DefaultComboBoxModel model = (DefaultComboBoxModel)cbmnv.getModel();
        for(NhanVien nv : dsnv){
            model.addElement(new ComboboxText(nv.getManv(), nv.getManv()));
        }
}

    public void hienthibangchamcong() throws SQLException {
        ChamCong dschamcong[] = new ChamCongRepository().ChamCongArray("");
        DefaultComboBoxModel model = (DefaultComboBoxModel)cbmbcc.getModel();
        for(ChamCong cg : dschamcong){
            model.addElement(new ComboboxText(cg.getMaBCC(),cg.getMaBCC()));
        }
    }
    
