    public void Binding(){
        int selected;
        boolean co,khong;
        
        selected = tbcd.getSelectedRow();
        
        tfmcd.setText(tbcd.getValueAt(selected, 0).toString());
        tflcd.setText(tbcd.getValueAt(selected, 1).toString());
        tftien.setText(tbcd.getValueAt(selected, 2).toString()); 
        co = tbcd.getValueAt(selected, 3).toString().equals("Có") ? true : false;
        khong = tbcd.getValueAt(selected, 3).toString().equals("Không") ? true : false;
    }
    public void hienthichedo(String search) throws SQLException {
        CheDo dscd[] = new CheDoRepository().CheDoArray(search);
        DefaultTableModel model = (DefaultTableModel)tbcd.getModel();
        model.setRowCount(0);
        for(CheDo cd : dscd){
            Object row[] = {cd.getMacd(),cd.getLoaicd(),cd.getTiencd(),cd.getBaoHiem()};
            model.addRow(row);
        }
    }
