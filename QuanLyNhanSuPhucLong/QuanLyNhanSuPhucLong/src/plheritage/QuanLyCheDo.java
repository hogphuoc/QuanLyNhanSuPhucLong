    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        String mcd,lcd,st,baohiem,message;
        
        mcd = tfmcd.getText();       
        lcd = tflcd.getText();
        st = tftien.getText();
        baohiem=rbco.isSelected() ? "Có" : "Không";
        CheDo cd = new CheDo(mcd, lcd, st, baohiem);
        
        try {
            CheDoRepository cdRepository = new CheDoRepository();
            // Thêm chế độ mới
            if(this.action.equals("add")){
                message = cdRepository.addCheDo(cd) ? "Thêm chế độ thành công" : "Thêm chế độ thất bại";
                JOptionPane.showMessageDialog(null, message, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
            // Sửa chế độ
            else if(this.action.equals("edit")){
                message = cdRepository.editCheDo(cd) ? "Sửa chế độ thành công" : "Sửa chế độ thất bại";
                JOptionPane.showMessageDialog(null, message, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
            hienthichedo("");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
        
        setEditable(false);
        btnadd.setEnabled(true);
        btnedit.setEnabled(true);
        btndelete.setEnabled(true);
        btnsave.setEnabled(false);
    }//GEN-LAST:event_btnsaveActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        int result = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa chế độ này?", "Thông báo", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
        if(result == 0){
            try {
                CheDoRepository cdRepository = new CheDoRepository();
                String macd = tfmcd.getText();
                if(cdRepository.deleteCheDo(macd)){
                    JOptionPane.showMessageDialog(null, "Xóa chế độ thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(null, "Xóa chế độ thất bại", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }
                hienthichedo("");
                Binding();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage(), "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            } catch (ArrayIndexOutOfBoundsException e){
            }
        }
    }//GEN-LAST:event_btndeleteActionPerformed

