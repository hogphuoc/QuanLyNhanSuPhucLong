    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        String manv , hoten , gioitinh , diachi , sdt , mapb , message;
        Date ngaysinh;
        hoten = tfhovaten.getText();
        manv = tfmanv.getText();
        gioitinh = rbtnam.isSelected() ? "Nam" : "Nữ";
        diachi = tfdiachi.getText();
        sdt = tfsdt.getText();
        ngaysinh = tfngaysinh.getDate();
        mapb = cbmpb.getSelectedItem().toString();
        
        NhanVien nv = new NhanVien(manv, hoten, gioitinh, diachi, sdt,(java.util.Date) ngaysinh, mapb);
        
        try {
            NhanVienRepository nhanVienRepository = new NhanVienRepository();
            // Thêm một nhân viên mới
            if(this.action.equals("add")){
                message = nhanVienRepository.addNhanVien(nv) ? "Thêm nhân viên thành công" : "Thêm nhân viên thất bại";
                JOptionPane.showMessageDialog(null, message, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
            // Sửa thông tin nhân viên
            else if(this.action.equals("edit")){
                message = nhanVienRepository.editNhanVien(nv) ? "Sửa nhân viên thành công" : "Sửa nhân viên thất bại";
                JOptionPane.showMessageDialog(null, message, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
            hienthinhanvien("");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
        
        setEditable(false);
        btnadd.setEnabled(true);
        btnedit.setEnabled(true);
        btndelete.setEnabled(true);
        btnsave.setEnabled(false);
    }//GEN-LAST:event_btnsaveActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        int result = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa nhân viên này?", "Thông báo", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
        if(result == 0){
            try {
                NhanVienRepository NVRepository = new NhanVienRepository();
                String manv = tfmanv.getText();
                if(NVRepository.deleteNhanVien(manv)){
                    JOptionPane.showMessageDialog(null, "Xóa nhân viên thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(null, "Xóa nhân viên thất bại", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }
                hienthinhanvien("");
                Binding();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage(), "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            } catch (ArrayIndexOutOfBoundsException e){
            }
        }
    }//GEN-LAST:event_btndeleteActionPerformed

