    public void hienthinhanvien(String search) throws SQLException {
        NhanVien dssv[] = new NhanVienRepository().NhanVienArray(search);
        DefaultTableModel model = (DefaultTableModel)tbNhanVien.getModel();
        model.setRowCount(0);
        for(NhanVien nv : dssv){
            Object row[] = {nv.getManv(), nv.getHoten(), nv.getGioitinh(), nv.getDiachi(), nv.getSodt(), nv.getNgaysinh(), nv.getMapb()};
            model.addRow(row);
        }
    }
    public void hienthimaphong() throws SQLException {
        Phongban dspb[] = new PhongbanRepository().phongbanarray("");
        
        for(Phongban pb : dspb){
            cbmpb.addItem(pb.getMapb());
        }
    }

    
    public void Binding(){
        int selected;
        boolean nam, nu;
        
        selected = tbNhanVien.getSelectedRow();
        
        tfmanv.setText(tbNhanVien.getValueAt(selected, 0).toString());
        tfhovaten.setText(tbNhanVien.getValueAt(selected, 1).toString());
        
        nam = tbNhanVien.getValueAt(selected, 2).toString().equals("Nam") ? true : false;
        nu = tbNhanVien.getValueAt(selected, 2).toString().equals("Nữ") ? true : false;
        
        rbtnam.setSelected(nam);
        rbtnu.setSelected(nu);
               
        tfdiachi.setText(tbNhanVien.getValueAt(selected, 3).toString());
        tfsdt.setText(tbNhanVien.getValueAt(selected, 4).toString());
        try {
            SimpleDateFormat formater = new SimpleDateFormat("YYYY-MM-DD");
            
            Date ns=formater.parse(tbNhanVien.getValueAt(selected, 5).toString());
           
            tfngaysinh.setDate(ns);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "Lỗi định dạng ngày tháng", "Lỗi!", JOptionPane.ERROR_MESSAGE);
        }
     
        cbmpb.setSelectedItem(tbNhanVien.getValueAt(selected, 6));
    }
